const buildAllDeniedPermissions = () =>
  Object.values(ACTION_FIELD_MAP).reduce((result, fieldKey) => {
    result[fieldKey] = false;
    return result;
  }, {});

const grant = (...actionKeys) => {
  const permissions = buildAllDeniedPermissions();
  actionKeys.forEach((actionKey) => {
    const fieldKey = ACTION_FIELD_MAP[actionKey];
    if (fieldKey) {
      permissions[fieldKey] = true;
    }
  });
  return permissions;
};

const grantAll = () =>
  Object.values(ACTION_FIELD_MAP).reduce((result, fieldKey) => {
    result[fieldKey] = true;
    return result;
  }, {});

